          Shotokan (松涛館?) é um dos estilos de karatê que surgiu dos ensinamentos ministrados pelo mestre Gichin Funakoshi e por seu filho, Yoshitaka Funakoshi. O repertório técnico do estilo foi baseado do Shorin-ryu, mas, devido aos estudos empreendido pelo filho do mestre e sua influência, várias técnicas foram incorporadas e/ou modificadas, de modo a refletir o escopo almejado, que era o de valorizar mais o lado desportivo e físico como forma de promover o desenvolvimento pessoal.

